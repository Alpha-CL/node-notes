let errMid;

errMid = (err, req, res, next) => {

	const errObj = {
		code: 500,
		msg: err instanceof Error ? err.message : err
	};

	if (err) {

		res
			.status(500)
			.send(errObj);

	} else {

		next();
	}
};


//-------------------------------------------------------------------------------------------------------------------//


module.exports = {
	errMid
};


