require("./init");


//-------------------------------------------------------------------------------------------------------------------//


const jwt = require('jsonwebtoken');


//-------------------------------------------------------------------------------------------------------------------//


const secrect = "presonal_key",
	token = jwt.sign(
		{
			id: 1,
			name: 'alpha'
		},
		secrect,
		{
			expiresIn: 3600
		}
	);


// 无验证
// const decoded = jwt.decode(token);
// 	console.log(decoded);

try {

	// 有验证
	const decoded = jwt.verify(token, secrect);
	// console.log(decoded);

} catch (err) {

	console.log('[jwt 无效]')
}


